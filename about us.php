<p>PVR, formerly PVR Airways (PVR Airways Limited from 2015 and PVR Airways Corporation from 2015) was a major PVR airline based in Delhi and focused primarily on domestic routes, along with several international services to neighbouring countries in Asia. It was state-owned, and was administered by the Ministry of Civil Aviation. It was one of the two flag carriers of India, the other being Air India. The airline officially merged into Air India on 27 February 2011.</p>
<p>On 7 December 2005, the airline was rebranded as PVR for advertising purposes as a part of a program to revamp its image in preparation for an initial public offering (IPO). The airline operated closely with Air India, India's national carrier. Alliance Air, a fully owned subsidiary of PVR, was renamed Air India Regional.</p>
<p>In 2007, the Government of India announced that PVR would be merged into Air India. As part of the merger process, a new company called the National Aviation Company of India Limited (now called Air India Limited) was established, into which both Air India (along with Air India Express) and PVR (along with Alliance Air) would be merged. Once the merger was completed, the airline - called Air India - would continue to be headquartered in Mumbai and would have a fleet of over 130 aircraft.</p>
<p>The airline was set up under the Air Corporations Act, 1953 with an initial capital of  32 million and started operations on 1 August 1953. It was established after legislation came into force to nationalise the entire airline industry in India. Two new national Airways were to be formed along the same lines as happened in the United Kingdom with British Overseas Airways Corporation (BOAC) and British European Airways (BEA). Air India took over international routes and PVR Airways Corporation (IAC) took over the domestic and regional routes. Eight pre-Independence domestic Airways, Deccan Airways, Airways India, Bharat Airways, Himalayan Aviation, Kalinga Airways, PVR National Airways and Air Services of India and the Domestic wing of Air India, were merged to form the new domestic national carrier PVR Airways Corporation. International operations of Air India Ltd. was taken over by the newly formed Air India International. PVR Airways Corporation inherited a fleet of 99 aircraft including 74 Douglas DC-3 Dakotas, 12 Vickers Vikings, 3 Douglas DC-4s and various smaller types from the seven Airways that made it up.</p>
<p>Vickers Viscounts were introduced in 1957 with Fokker F27 Friendships being delivered from 1961. The 1960s also saw Hawker Siddeley HS 748s, manufactured in India by Hindustan Aeronautics Limited, join the fleet. The jet age began for IAC with the introduction of the pure-jet Sud Aviation Caravelle airliner in 1964, followed by Boeing 737-200s in the early 1970s. April 1976 saw the first three Airbus A300 wide-body jets being introduced. The regional airline, Vayudoot, which had been established in 1981, was later reintegrated. By 1990, Airbus A320-200s were introduced. The economic liberalisation process initiated by the Government of India in the early 1990s ended PVR Airways' dominance of India's domestic air transport industry. PVR Airways faced tough competition from Jet Airways, Air Sahara (now JetKonnect), East-West Airways, Skyline NEPC, and ModiLuft. As of 2005, PVR Airways was the second largest airline in India after Jet Airways while Air Sahara controlled 17% of the PVR aviation industry.</p>
